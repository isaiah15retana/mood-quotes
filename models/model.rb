 def quote(mood)
  if mood=="happy"
    quote=["It's all about quality of life and finding a happy balance between work and friends and family."]
  elsif mood=="sad"
     quote=["It's all about quality of life and finding a happy balance between work and friends and family."]
 elsif mood=="loved"
     quote=["Love is when the other person's happiness is more important than your own."]
  elsif mood=="scared"
     quote= ["Its OKAY to be scared, but DONT LET that fear overpower your mind and control you"]  
elsif mood=="excited"
     quote=["Entusiasm is excitement with inspiration,motivation,and a pinch of creativity"]
     elsif  mood=="angry"
     quote=["You need advice? ask me! Feeling down ? Lean on me! Angry? Vent to me! Need a friend? I will be your friend!"]
   elsif mood=="lonely"
     quote=["We sometimes think we want to disappear,but all we really want is to be found."]
   end 
 end 